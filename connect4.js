/** Connect Four
 *
 * Player 1 and 2 alternate turns. On each turn, a piece is dropped down a
 * column until a player gets four-in-a-row (horiz, vert, or diag) or until
 * board fills (tie)
 */

const WIDTH = 7;
const HEIGHT = 6;

let currPlayer = 1; // active player: 1 or 2
const board = []; // array of rows, each row is array of cells  (board[y][x])

/** makeBoard: create in-JS board structure:
 *    board = array of rows, each row is array of cells  (board[y][x])
 */

function makeBoard() {
  for (let i = 0; i < HEIGHT; i++) {
    let newRow = [];
    board.push(newRow)
    for (let j = 0; j < WIDTH; j++) {
      newRow.push(null)
    }
  }
}

/** makeHtmlBoard: make HTML table and row of column tops. */

function makeHtmlBoard() {
  const htmlBoard = document.querySelector('#board');

  // Creates the interative row that sits above the game board.
  // The element is created and given the id that corressponds with the 'column-top' CSS class
  // An event listener is added to check for clicks
  const top = document.createElement("tr");
  top.setAttribute("id", "column-top");
  top.addEventListener("click", handleClick);

  // Creates the cells in the top row and gives a dynamic id
  for (let x = 0; x < WIDTH; x++) {
    const headCell = document.createElement("td");
    headCell.setAttribute("id", x);
    top.append(headCell);
  }
  htmlBoard.append(top);

  // Creates the rows and the cells for the game board. Each cell is given a dynamic Y-X id
  // The top left cell of the board is 5-0, with X increasing to the left, and Y decreading downwards
  // I reversed the iteration order in the y loop because the bottom column was originally 5 instead of 0
  // I will talk to my mentor to see if there is anything I could have done differently
  for (let y = HEIGHT - 1; y >= 0; y--) {
    const row = document.createElement("tr");
    for (let x = 0; x < WIDTH; x++) {
      const cell = document.createElement("td");
      cell.setAttribute("id", `${y}-${x}`);
      row.append(cell);
    }
    htmlBoard.append(row);
  }
}

